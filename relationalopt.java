public class relationalopt {
    public static void main(String[] args) {
        int a=10,b=20;
        System.out.println("Logical operations");
        System.out.println("A < B: "+(a<b));
        System.out.println("A > B: "+(a>b));
        System.out.println("A != B: "+(a!=b));
        System.out.println("A == B: "+(a==b));
        System.out.println("A >= B: "+(a>=b));
        System.out.println("A <= B: "+(a<=b));
    }
}
