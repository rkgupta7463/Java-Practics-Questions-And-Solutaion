public class logical {
    public static void main(String[] args) {
        int a=10,b=15;
        System.out.println("Logical operators");
        System.out.println("A != B : "+(a != b));
        System.out.println("A & B : "+(a & b));
        System.out.println("A == B : "+(a == b));
    }
}
