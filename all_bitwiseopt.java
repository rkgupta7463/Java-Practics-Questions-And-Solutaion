public class all_bitwiseopt {
    public static void main(String[] args) {
        int a=12, b=15;
        System.out.println("All Bitwise data type");
        System.out.println("A & B "+(a&b));
        System.out.println("A ~ "+(~a));
        System.out.println("B ~ "+(~b));
        System.out.println("A | B "+(a|b));
        System.out.println("A ^ B "+(a ^ b));
        System.out.println("A >> B "+(a >> b));
        System.out.println("A << B "+(a << b));
    }    
}
